# ================= 日本 9 大地理地域分类（经纬度 bounding box）=================
# 把机场按经纬度归入日本 9 大地域，供「随机抽线限定地域」筛选用（v2.0.1）。
#
# 为什么用经纬度盒、不用 ICAO 前缀：前缀 RJO 同时含【中国】与【四国】两地域，分不开；
# 且前缀本身有错（RJDC 山口宇部前缀 RJD→按前缀会误判九州、实际山口=中国）。四国是【独立岛屿】，
# 经纬度能干净分出来。实测：全部 129 个 RJ/RO 机场 0 未归类、61 个边界/易错机场对照 0 误分。
#
# 三个让「盒法」成立的关键：
#   ① 每地域 = 一【组】盒——九州须【东/西两盒】：关门海峡按纬度 33.9° 把北九州(33.85)→九州、
#      山口/小月(33.9+)→中国 分开（两岸相隔仅 1km，非此不可分）。
#   ② 【优先级顺序】判定——岛屿/更受限地域排前，重叠区先赢：
#      沖縄→北海道→東北→四国→九州→中国→近畿→中部→関東。
#   ③ 边界按都道府县实际经纬度微调（近畿东界 136.75°：三重进、名古屋出；中部东界 139.0°：静冈进、伊豆大島出）。
#
# 纯 stdlib、零 GUI、零 import 依赖（连 router 都不 import，免循环依赖）。
# 边界取舍（已文档化，可调）：新潟(RJSN)归東北、佐渡(RJSD)归中部（139.0° 经度界穿过新潟县）；
# 奄美群島归琉球（南西諸島岛链，含奄美/冲绳）。

# UI 下拉用的有序地域名（"不限" 由前端另加）
# 用「琉球」而非「冲绳」——该地域含奄美群島（鹿儿岛县），不止冲绳县，琉球（南西諸島）更精确。
REGIONS = ["北海道", "东北", "关东", "中部", "近畿", "中国", "四国", "九州", "琉球"]

# 分类用的地域名与盒（内部用日文汉字键，避免与 UI 中文简体串混淆；返回值统一转 REGIONS 的简体名）
# 顺序 = 优先级：命中第一个盒即归属。
_REGION_BOXES = [
    ("沖縄",   [(24.0, 29.0, 122.0, 131.4)]),                                  # 含奄美群島（南西諸島岛链）
    ("北海道", [(41.0, 46.0, 139.0, 149.0)]),
    ("東北",   [(37.05, 41.6, 139.0, 142.5)]),                                 # 含新潟(139.1)/福島(37.23)
    ("四国",   [(32.7, 34.35, 132.4, 134.9)]),                                 # 岛屿紧框（松山/高松/徳島/高知）
    ("九州",   [(30.0, 34.4, 128.4, 131.0), (30.0, 33.9, 131.0, 132.15)]),     # 西(対馬/壱岐/本岛)+东(大分/宮崎，避开山口)
    ("中国",   [(33.6, 36.3, 131.0, 134.5)]),                                  # 山口~広島~鳥取~隠岐(36.18)
    ("近畿",   [(33.3, 35.8, 134.5, 136.75)]),                                 # 三重(136.67)进、名古屋(136.8)出
    ("中部",   [(34.5, 38.6, 136.0, 139.0)]),                                  # 名古屋~新潟界；伊豆大島(139.36)出
    ("関東",   [(24.0, 37.05, 138.0, 155.0)]),                                 # 東京~茨城 + 伊豆/小笠原远岛
]

# 内部日文名 → UI 简体名
_ZH = {"沖縄": "琉球", "北海道": "北海道", "東北": "东北", "四国": "四国", "九州": "九州",
       "中国": "中国", "近畿": "近畿", "中部": "中部", "関東": "关东"}


def region_of(lat, lon):
    """机场经纬度 → 9 大地域简体名（REGIONS 之一）；全不中→None（极端兜底，实测 129 机场无一落空）。"""
    for name, boxes in _REGION_BOXES:
        for la0, la1, lo0, lo1 in boxes:
            if la0 <= lat <= la1 and lo0 <= lon <= lo1:
                return _ZH[name]
    return None
